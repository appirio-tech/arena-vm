package com.topcoder.server.common;

import java.io.Serializable;
import java.sql.Timestamp;
import java.util.ArrayList;

public class VerifyContest implements Serializable {

    private String m_cacheKey;

    public final String getCacheKey() {
        return m_cacheKey;
    }

    private int contestId;
    private String contestName;
    private Timestamp startDate;
    private Timestamp endDate;
    private String status;
    private int languageId;
    private int groupId;
    private String regionCode;
    private String adText;
    private Timestamp adStart;
    private Timestamp adEnd;
    private String adTask;
    private String adCommand;
    private int activateMenu;
    private ArrayList rounds; // ArrayList of Round Attributes
    private boolean modified;


    ////////////////////////////////////////////////////////////////////////////////
    public VerifyContest()
            ////////////////////////////////////////////////////////////////////////////////
    {
        this.contestId = -1;
        this.contestName = "";
        this.startDate = null;
        this.endDate = null;
        this.status = "";
        this.languageId = -1;
        this.groupId = -1;
        this.regionCode = "";
        this.adText = "";
        this.adStart = null;
        this.adEnd = null;
        this.adTask = "";
        this.adCommand = "";
        this.activateMenu = 0;
        this.rounds = new ArrayList();
        this.modified = false;

    }

    public static String getCacheKey(int contestId) {
        return "Contest." + contestId;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setContestId(int in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.contestId = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setContestName(String in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.contestName = in;
    }


    ////////////////////////////////////////////////////////////////////////////////
    public final void setStartDate(Timestamp in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.startDate = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setEndDate(Timestamp in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.endDate = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setStatus(String in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.status = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setLanguageId(int in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.languageId = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setGroupId(int in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.groupId = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setRegionCode(String in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.regionCode = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setAdText(String in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.adText = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setAdStart(Timestamp in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.adStart = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setAdEnd(Timestamp in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.adEnd = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setAdTask(String in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.adTask = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setAdCommand(String in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.adCommand = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setActivateMenu(int in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.activateMenu = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setRounds(ArrayList in) {
        ////////////////////////////////////////////////////////////////////////////////
        this.rounds = in;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final void setModified(boolean modified) {
        ////////////////////////////////////////////////////////////////////////////////
        this.modified = modified;
    }


    ////////////////////////////////////////////////////////////////////////////////
    public final int getContestId() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.contestId;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final String getContestName() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.contestName;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final Timestamp getStartDate() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.startDate;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final Timestamp getEndDate() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.endDate;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final String getStatus() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.status;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final int getLanguageId() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.languageId;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public int getGroupId() {
        ////////////////////////////////////////////////////////////////////////////////
        return groupId;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final String getRegionCode() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.regionCode;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final String getAdText() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.adText;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final Timestamp getAdStart() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.adStart;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final Timestamp getAdEnd() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.adEnd;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final String getAdTask() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.adTask;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final String getAdCommand() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.adCommand;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final int getActivateMenu() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.activateMenu;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final ArrayList getRounds() {
        ////////////////////////////////////////////////////////////////////////////////
        return this.rounds;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public final boolean getModified() {
        ////////////////////////////////////////////////////////////////////////////////
        return modified;
    }

    ////////////////////////////////////////////////////////////////////////////////
    public String toString()
            ////////////////////////////////////////////////////////////////////////////////
    {
        StringBuffer str = new StringBuffer(500);
        str.append((new Integer(contestId)).toString());
        str.append(", ");
        str.append(contestName);
        str.append(",");
        str.append(startDate);
        str.append(", ");
        str.append(endDate);
        str.append(", ");
        str.append(status);
        str.append(", ");
        str.append(rounds.toString());
        str.append(", ");
        str.append(activateMenu);
        return str.toString();
    }

    ////////////////////////////////////////////////////////////////////////////////
    public boolean equals(VerifyContest contest)
            ////////////////////////////////////////////////////////////////////////////////
    {
        return this.contestId == contest.getContestId();
    }
}
