        str.append(",");
        str.append(methodName);
        str.append(", ");
        str.append(resultType);
        str.append(", ");
        str.append(paramTypes.toString());
        str.append(", ");
        str.append(defaultSolution);
        str.append(", ");
        str.append(problemText);
        //    str.append(", ");
        //    str.append(language);
        str.append(", ");
        str.append(testCases);
        return str.toString();
